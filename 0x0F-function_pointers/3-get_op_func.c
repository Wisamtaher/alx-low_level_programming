#include "3-calc.h"

/**
 * op_add - sum
 * @a: variable
 * @b: variable
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction
 * @a: variable
 * @b: variable
 * Return: subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication
 * @a: variable
 * @b: variable
 * Return: multiplication
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divison
 * @a: variable
 * @b: variable
 * Return: divison
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - mod
 * @a: variable
 * @b: variable
 * Return: mod
 */

int op_mod(int a, int b)
{
	return (a % b);
}
